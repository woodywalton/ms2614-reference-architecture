// Detect which NDJSON schema a parsed line belongs to
export function classifyNdjsonLine(obj) {
  if (obj.type && obj.attributes !== undefined) {
    // Kibana saved object export format
    return {
      displayType: obj.type,
      title: obj.attributes?.title ?? obj.id ?? obj.type,
    }
  }
  if (obj.rule_id !== undefined || obj.name !== undefined) {
    // Elastic Security rule export format
    return {
      displayType: obj.type ?? 'rule',
      title: obj.name ?? obj.rule_id ?? obj.id,
    }
  }
  return {
    displayType: obj.type ?? 'object',
    title: obj.id ?? obj.name ?? 'object',
  }
}
